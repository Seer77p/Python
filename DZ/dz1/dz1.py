# dz-1.8
# Вы пользуетесь общественным транспортом? Вероятно, вы
# расплачивались за проезд и получали билет с номером. Счастливым
# билетом называют такой билет с шестизначным номером, где сумма
# первых трех цифр равна сумме последних трех. Т.е. билет с номером
# 385916 – счастливый, т.к. 3+8+5=9+1+6. Вам требуется написать
# программу, которая проверяет счастливость билета.
# 385916 -> yes
# 123456 -> no


bilet = int(input('Введите 6 значное число: '))
while (100000 > bilet or bilet > 999999):
    print('Вы ввели не шестизначное число, \n')
    bilet = int(input('Введите шестизначное число: '))
left = bilet//1000
right = bilet % 1000
left = int(left//100)+int(left//10 % 10)+int(left % 10)
right = int(right//100)+int(right//10 % 10)+int(right % 10)
print('Сумма цифр левой части числа= ', left, '\n',
      'Сумма цифр правой части числа= ', right)
if left == right:
    print('Сумма правой и левой части номера билета равны')
else:
    print('Сумма правой и левой части номера билета не равны')
