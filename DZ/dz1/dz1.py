coordinate_x1 = int(input('Введите координату x1= '))
coordinate_y1 = int(input('Введите координату y1= '))
coordinate_x2 = int(input('Введите координату x2= '))
coordinate_y2 = int(input('Введите координату y2= '))
while (coordinate_x1 == coordinate_x2 and coordinate_y1 == coordinate_y2):
    print('Вы ввели одинаковые координаты, расстояние = 0, \nВведите разные координаты!')
    coordinate_x1 = int(input('Введите координату x1= '))
    coordinate_y1 = int(input('Введите координату y1= '))
    coordinate_x2 = int(input('Введите координату x2= '))
    coordinate_y2 = int(input('Введите координату y2= '))
distance = math.sqrt(math.pow(coordinate_x2-coordinate_x1,
                     2)+math.pow(coordinate_y2-coordinate_y1, 2))
print('Расстояние между точками в 2D пространстве составляет = ', round((distance), 3))

