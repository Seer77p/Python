# Напишите программу, которая принимает на вход координаты точки (X и Y), причём X ≠ 0 и Y ≠ 0 и выдаёт номер
# четверти плоскости, в которой находится эта точка (или на какой оси она находится).
# Пример:
# - x=34; y=-30 -> 4
# - x=2; y=4-> 1
# - x=-34; y=-30 -> 3
coordinate_x = int(input('Введите координату x= '))
coordinate_y = int(input('Введите координату y= '))
while coordinate_x == 0 and coordinate_y == 0:
    print('Введенные координаты равны 0, введите координаты отличные 0')
    coordinate_x = int(input('Введите координату x= '))
    coordinate_y = int(input('Введите координату y= '))
if (coordinate_x > 0 and coordinate_y > 0):
    print('Введенные координаты соответствуют (X от 0 до бесконечности и Y от 0 до бесконечности ) 1 четверти')
elif (coordinate_x > 0 and coordinate_y < 0):
    print('Введенные координаты соответствуют (X от 0 до бесконечности и Y от 0 до минус бесконечности ) 4 четверти')
elif (coordinate_x < 0 and coordinate_y < 0):
    print('Введенные координаты соответствуют (X от 0 до минус бесконечности и Y от 0 до минус бесконечности ) 3 четверти')
elif (coordinate_x < 0 and coordinate_y > 0):
    print('Введенные координаты соответствуют (X от 0 до минус бесконечности и Y от 0 до бесконечности ) 2 четверти')


